// string to integer
var a = "123.2";
var b = parseInt(a);
console.log(b); // 123

// string to float
var a = "123.2";
var b = parseFloat(a);
console.log(b); // 123.2

// integer to string
var a = 123;
var b = a.toString();
console.log(b); // "123"

// float to string
var a = 123.2;
var b = a.toString();
console.log(b); // "123.2"

// float to integer
var a = 123.2;
var b = parseInt(a);
console.log(b); // 123

// integer to float
var a = null;
var b = parseFloat(a);
console.log(b); // null







