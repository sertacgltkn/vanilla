// Operators

var a = "1";

console.log(a==1) // true

console.log(a===1) // false

console.log(a!=1) // false

console.log(a!==1) // true

console.log(a>1) // false

console.log(a<1) // false

console.log(a>=1) // true

console.log(a<=1) // true

console.log(a&&1) // 1

console.log(a||1) // 1

console.log(!a) // false

console.log(a&1) // 1

console.log(a|1) // 1

console.log(a^1) // 0

console.log(a<<1) // 2

console.log(a>>1) // 0

console.log(a>>>1) // 0

console.log(a+=1) // 2

console.log(a-=1) // 1

console.log(a*=1) // 1

console.log(a/=1) // 1

console.log(a%=1) // 0

console.log(a&=1) // 1
